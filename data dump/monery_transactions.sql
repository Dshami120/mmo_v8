CREATE TABLE `monery_transactions` (
  `SYS_TRANSACTION_ID` int(11) NOT NULL AUTO_INCREMENT,
  `SYS_CREATE_DATE` timestamp NOT NULL DEFAULT current_timestamp(),
  `SYS_UPDATE_DATE` timestamp NOT NULL DEFAULT current_timestamp(),
  `SYS_USER_ID` int(11) NOT NULL,
  `TO_ACCOUNT_ID` int(11) NOT NULL,
  `FROM_ACCOUNT_ID` int(11) DEFAULT NULL,
  `TRANSACTION_DATE` date NOT NULL,
  `TRANSACTION_AMOUNT` decimal(12,2) NOT NULL DEFAULT 0.00,
  `TRANSACTION_DESC` varchar(200) DEFAULT NULL,
  `TRANSACTION_CATEGORY` varchar(45) NOT NULL,
  PRIMARY KEY (`SYS_TRANSACTION_ID`),
  UNIQUE KEY `SYS_TRANSACTION_ID_UNIQUE` (`SYS_TRANSACTION_ID`)
)