CREATE TABLE `money_account` (
  `SYS_ACCOUNT_ID` int(11) NOT NULL AUTO_INCREMENT,
  `SYS_CREATE_DATE` timestamp NOT NULL DEFAULT current_timestamp(),
  `SYS_UPDATE_DATE` timestamp NOT NULL DEFAULT current_timestamp(),
  `SYS_USER_ID` int(11) NOT NULL,
  `ACCOUNT_TYPE` varchar(50) NOT NULL,
  `ACCOUNT_NAME` varchar(50) NOT NULL,
  `MONTHLY_LIMIT` decimal(12,2) DEFAULT NULL,
  `ACCOUNT_START_BALANCE` decimal(12,2) NOT NULL DEFAULT 0.00,
  PRIMARY KEY (`SYS_ACCOUNT_ID`),
  UNIQUE KEY `SYS_ACCOUNT_ID_UNIQUE` (`SYS_ACCOUNT_ID`)
)